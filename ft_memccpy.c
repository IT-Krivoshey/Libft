#include "libft.h"

void			*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	char		*s1;
	const char	*s2;

	s1 = dst;
	s2 = src;
	while (n--)
	{
		*s1++ = *s2++;
		if (*(s1 - 1) == (char)c)
			return (s1);
	}
	return (NULL);
}
